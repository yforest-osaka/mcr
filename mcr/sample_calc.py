def add(a: float, b: float) -> float:
    """2つの数値の和を返す関数

    Args:
        a (float): 数値1
        b (float): 数値2

    Returns:
        float: a+b
    """
    return a + b


def mult(a: float, b: float) -> float:
    """2つの数値の積を返す

    Args:
        a (float): 数値1
        b (float): 数値2

    Returns:
        float: a*b
    """
    return a * b
